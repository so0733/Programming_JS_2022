const score = 90;
if (score >= 90) {        // 90점 이상
    console.log('A+');
} else if (score >= 80) { // 80점 이상
    console.log('A');
} else if (score >= 70) { // 70점 이상
    console.log('B+');
} else if (score >= 60) { // 60점 이상
    console.log('B');
} else {                  // 60점 미만
    console.log('F')
}
